        for (let i = 11; i >= 0; i--) {
          if (dd > 11) {
            show = dd - i
          } else {
            const lasti = i - dd // 上个月需要计算的天数
            show = lastday - lasti
            if (show > lastday) { // 上月已满
              show = dd - i
            }
          }
          data.push(show)
        }
        return data
      }())
